#include <iostream>
using namespace std;
 
int main() {
  // We are declaring two variable of the same type(integer).
  int num1, num2;
  /*
  << => Insertion Operator.
  >> => Extraction Operator.
  endl => End line to display the next command on a new line
  */
  cout << "Enter the value of num1: " << endl;
  cin >> num1;
  cout << "Enter the value of num2: " << endl;
  cin >> num2;
  cout << "The sum is "<< num1+num2 << endl;
  
  return 0;
}
 
